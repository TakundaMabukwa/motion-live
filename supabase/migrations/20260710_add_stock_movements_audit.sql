$$;

-- ============================================================
-- 4. TRIGGERS
-- ============================================================

CREATE TRIGGER trg_log_inventory_items_movement
  AFTER INSERT OR UPDATE OR DELETE ON public.inventory_items
  FOR EACH ROW
  EXECUTE FUNCTION public.log_inventory_items_movement();

CREATE TRIGGER trg_log_client_inventory_items_movement
  AFTER INSERT OR UPDATE OR DELETE ON public.client_inventory_items
  FOR EACH ROW
  EXECUTE FUNCTION public.log_client_inventory_items_movement();

CREATE TRIGGER trg_log_tech_stock_movement
  AFTER UPDATE ON public.tech_stock
  FOR EACH ROW
  WHEN (OLD.assigned_parts IS DISTINCT FROM NEW.assigned_parts)
  EXECUTE FUNCTION public.log_tech_stock_movement();

CREATE TRIGGER trg_log_job_cards_stock_movement
  AFTER UPDATE ON public.job_cards
  FOR EACH ROW
  WHEN (OLD.parts_required IS DISTINCT FROM NEW.parts_required
        OR OLD.equipment_used IS DISTINCT FROM NEW.equipment_used)
  EXECUTE FUNCTION public.log_job_cards_stock_movement();
