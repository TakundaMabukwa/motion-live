        v_serial,
        v_item->>'category_code',
        'PART_ADDED_TO_EQUIPMENT_USED',
        CASE
          WHEN v_source = 'tech_stock.assigned_parts' THEN 'tech'
          WHEN v_source = 'inventory_items' THEN 'soltrack'
          WHEN v_source LIKE 'client_inventory_items%' THEN 'client'
