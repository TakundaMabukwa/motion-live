-- ============================================================
-- Migration: DB-Level Stock Movement Audit Trail
-- ============================================================
-- Creates a stock_movements table and 4 triggers that log
-- every stock change across all 4 buckets:
--   1. inventory_items (Soltrack central stock)
--   2. client_inventory_items (Client-level stock)
--   3. tech_stock (Technician boot stock - assigned_parts)
--   4. job_cards (parts_required + equipment_used)
--
-- Zero app code changes. Pure database triggers.
-- Search by serial_number to see the full journey of any part.
-- ============================================================

-- ============================================================
-- 1. TABLE
-- ============================================================
CREATE TABLE IF NOT EXISTS public.stock_movements (
  id bigint GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  serial_number text NOT NULL,
  category_code text,
  operation text NOT NULL,
  from_bucket text,
  to_bucket text,
  from_owner text,
  to_owner text,
  from_field text,
  to_field text,
  client_code text,
  cost_code text,
  job_card_id uuid,
  job_number text,
  quantity integer DEFAULT 1,
  old_status text,
  new_status text,
  old_assigned_to text,
  new_assigned_to text,
  old_data jsonb,
  new_data jsonb,
  diff jsonb,
  CONSTRAINT stock_movements_pkey PRIMARY KEY (id)
);

-- ============================================================
-- 2. INDEXES
-- ============================================================
CREATE INDEX IF NOT EXISTS idx_stock_movements_serial
  ON public.stock_movements (serial_number);

CREATE INDEX IF NOT EXISTS idx_stock_movements_job_card
  ON public.stock_movements (job_card_id);

CREATE INDEX IF NOT EXISTS idx_stock_movements_from_bucket
  ON public.stock_movements (from_bucket);

CREATE INDEX IF NOT EXISTS idx_stock_movements_to_bucket
  ON public.stock_movements (to_bucket);

CREATE INDEX IF NOT EXISTS idx_stock_movements_from_owner
  ON public.stock_movements (from_owner);

CREATE INDEX IF NOT EXISTS idx_stock_movements_to_owner
  ON public.stock_movements (to_owner);

CREATE INDEX IF NOT EXISTS idx_stock_movements_created_at
  ON public.stock_movements (created_at DESC);

CREATE INDEX IF NOT EXISTS idx_stock_movements_operation
  ON public.stock_movements (operation);

-- ============================================================
-- 3. TRIGGER FUNCTIONS
-- ============================================================

-- ----------------------------------------------------------
-- 3a. inventory_items trigger
-- ----------------------------------------------------------
CREATE OR REPLACE FUNCTION public.log_inventory_items_movement()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  v_from_bucket text;
  v_to_bucket text;
  v_from_owner text;
  v_to_owner text;
  v_job_number text;
BEGIN
  IF TG_OP = 'INSERT' THEN
    v_to_bucket := 'soltrack';

    IF NEW.assigned_to_technician IS NOT NULL THEN
      v_to_bucket := 'tech';
      v_to_owner := NEW.assigned_to_technician;
    END IF;

    IF NEW.job_card_id IS NOT NULL THEN
      v_to_bucket := 'job';
      SELECT jc.job_number INTO v_job_number
      FROM public.job_cards jc WHERE jc.id = NEW.job_card_id;
    END IF;

    INSERT INTO public.stock_movements (
      serial_number, category_code, operation,
      from_bucket, to_bucket, to_owner,
      job_card_id, job_number,
      new_status, new_assigned_to, new_data
    ) VALUES (
      NEW.serial_number, NEW.category_code, 'INSERT',
      NULL, v_to_bucket, v_to_owner,
      NEW.job_card_id, v_job_number,
      NEW.status, NEW.assigned_to_technician, to_jsonb(NEW)
    );

    RETURN NEW;
  END IF;

  IF TG_OP = 'UPDATE' THEN
    IF OLD.status IS DISTINCT FROM NEW.status
       OR OLD.assigned_to_technician IS DISTINCT FROM NEW.assigned_to_technician
       OR OLD.job_card_id IS DISTINCT FROM NEW.job_card_id THEN

      v_from_bucket := 'soltrack';
      v_to_bucket := 'soltrack';

      IF OLD.assigned_to_technician IS DISTINCT FROM NEW.assigned_to_technician THEN
        IF NEW.assigned_to_technician IS NOT NULL THEN
          v_to_bucket := 'tech';
          v_to_owner := NEW.assigned_to_technician;
        ELSE
          v_from_bucket := 'tech';
          v_from_owner := OLD.assigned_to_technician;
        END IF;
      END IF;

      IF OLD.job_card_id IS DISTINCT FROM NEW.job_card_id THEN
        IF NEW.job_card_id IS NOT NULL THEN
          v_to_bucket := 'job';
          SELECT jc.job_number INTO v_job_number
          FROM public.job_cards jc WHERE jc.id = NEW.job_card_id;
        ELSE
          v_from_bucket := 'job';
        END IF;
      END IF;

      INSERT INTO public.stock_movements (
        serial_number, category_code, operation,
        from_bucket, to_bucket, from_owner, to_owner,
        job_card_id, job_number,
        old_status, new_status,
        old_assigned_to, new_assigned_to,
        old_data, new_data
      ) VALUES (
        NEW.serial_number, NEW.category_code, 'UPDATE',
        v_from_bucket, v_to_bucket, v_from_owner, v_to_owner,
        NEW.job_card_id, v_job_number,
        OLD.status, NEW.status,
        OLD.assigned_to_technician, NEW.assigned_to_technician,
        to_jsonb(OLD), to_jsonb(NEW)
      );
    END IF;

    RETURN NEW;
  END IF;

  IF TG_OP = 'DELETE' THEN
    INSERT INTO public.stock_movements (
      serial_number, category_code, operation,
      from_bucket, to_bucket,
      old_status, old_assigned_to, old_data
    ) VALUES (
      OLD.serial_number, OLD.category_code, 'DELETE',
      'soltrack', NULL,
      OLD.status, OLD.assigned_to_technician, to_jsonb(OLD)
    );

    RETURN OLD;
  END IF;

  RETURN NULL;
END;
$$;

